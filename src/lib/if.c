int b2t(void *tbuf, void *bbuf, int size, char white_space)
{
        int i;
        const char *ch;
        unsigned char *pt = (unsigned char *)tbuf;
        unsigned char *pb = (unsigned char *)bbuf;

        for(i = 0; i < size; i++)
        {
                ch = b2t_table[*pb++];

                *pt++ = *ch++;
                *pt++ = *ch;
                *pt++ = white_space;
        }
        *pt = '\0';

        return 0;
}

int t2b(void *bbuf, void *tbuf)
{
        int i;
        unsigned char h, l, x;
        unsigned char *pb = (unsigned char *)bbuf;
        unsigned char *pt = (unsigned char *)tbuf;

        //puts(pt);

        for(i = 0; (1); i++)
        {
                h = *pt++;
                if('\0' == h || 0x0a == h || 0x0d == h) break;

                l = *pt++;
                if('\0' == l || 0x0a == l || 0x0d == l) break;

                *pb++ = t2b_table_h[h] | t2b_table_l[l];
                //printf("%02X ", (int)pb[b]);

                x = *pt++;
                if('\0' == x || 0x0a == x || 0x0d == x)
                {
                        break;
                }
                else if((('0' <= x) && (x <= '9')) ||
                        (('A' <= x) && (x <= 'F')) ||
                        (('a' <= x) && (x <= 'f'))
                )
                {
                        fprintf(stderr, "Bad white space: 0x%02X\n", (int)x);
                        fprintf(stderr, "%s\n", (char *)tbuf);
                        return -1;
                }
        }
        *pt = '\0';

        return i;
}

